package day19_LoopPractices;

public class LoopsWithoutCurlyBraces {

        public static void main(String[] args) {

            for (int i = 0; i < 5; i++)
                System.out.println("Cydeo");
            System.out.println("Batch 25");


            /*
            we can use for loops without curly braces , but it will only repeat the first statement for the
        predetermined number of iterations. (just like if statement, it can be written without the curly braces
        however, it will only accept one condition and one statement.

        As a good practice never write a for loop nor an if statement without the curly braces!
             */











        }
}
