package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {
        if (true){
            System.out.println("Wooden Spoon"); // executed one time
        }

        System.out.println("------------------------------------------------");
        while (true){
            System.out.println("Wooden Spoon");
// it will be executed for infinite times because we haven't determined the number of times it should be executed!

        }


    }
}
